				</li>
			<li id="1930">
				<img src="includes/img/timeline/2.png" width="210" height="210" />
				<h1>1933</h1>
			<p>En 1933, un médecin de Viennes, nommé Manfred Sakel, eut l'idée de provoquer des comas insuliniques sur des personnes schizophrènes. Ce traitement fut toutefois abandonné à la suite des angoisses importantes causées aux patients.</p>
			</li>
			<li id="1944">
				<img src="includes/img/timeline/3.png" width="210" height="210" />
				<h1>1936</h1>
				<p>Le choc au cardiazol, mis en place par L.J. Von Meduna, en 1936...</p>
				</li>
			<li id="1950">
				<img src="includes/img/timeline/4.png" width="210" height="210" />
				<h1>1938</h1>
				<p>Ugo Cerletti et Lucio Bini reprirent en 1938 l'idée de leur prédécesseur mais décidèrent de remplacer le curare par des chocs électriques, donnant naissance à la sismothérapie, c'est donc à cette date que la première sismothérapie fut pratiquée. Les résultats furent cependant très médiocres.</p>
				</li>
			<li id="1971">
				<img src="includes/img/timeline/5.png" width="210" height="210" />
				<h1>1975</h1>
				<p>"Vol au dessus d'un nid de coucou" est un film américain de Milos Forman sorti en 1975, le film met en scène une personne mentalement dérangée soignée grâce à des électrochocs localisés sur la boite cranienne.</p>
			</li>
			<li id="1977">
				<img src="includes/img/timeline/6.png" width="210" height="240" />
				<h1>1985</h1>
				<p>En 1985, le National Institute Of Health affirme l'efficacité de la sismothérapie dans les épisodes dépressifs majeurs. En effet, ce traitement est aussi efficace que les antidépresseurs de référence dans les épisodes de dépressions majeures, et est aussi efficace que le lithium dans le traitement de l'accès maniaque.</p>
				</li>
			<li id="1989">
				<img src="includes/img/timeline/7.png" width="210" height="210" />
				<h1>1997</h1>
				<p>En 1997, Un texte de l'ANAES et de la FFP fait une synthèse rigoureuse des connaissances scientifiques sur la sismothérapie. Depuis les connaissances s'accumulent quant aux voies d'actions, aux indications, aux techniques et aux résultats, mais un flou reste encore sur la réaction à long terme sur le corps humain.</p>
				</li>
			<li id="1999">
				<img src="includes/img/timeline/8.png" width="210" height="210" />
				<h1>1999</h1>
				<p> Les premières contre-indications sont exposées en public, de nombreuses personnes véhiculent depuis un certain temps une image de cette thérapie barbare, ainsi des associations combattent ces soins, certains hopitaux refusent de pratiquer cette thérapie, la controverse commence à atteindre le citoyen.</p>
				</li>
			<li id="2011">
				<img src="includes/img/timeline/9.gif" width="210" height="210" />
				<h1>2001</h1>
				<p> En 2001 la controverse prend de l'ampleur, l'Angleterre, le Québec, et maintenant la France, prennent aux sérieux les différents dangers de la thérapie, cependant un flou entoure ce traitement, peu d'informations apparaissent pour le grand public.</p>
				</li>
